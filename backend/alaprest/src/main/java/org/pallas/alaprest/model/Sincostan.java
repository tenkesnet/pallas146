package org.pallas.alaprest.model;

import java.util.Date;

public class Sincostan {

    private double arcSin;

    private double arcCos;

    private double arcTan;

    public double getArcSin() {
        return arcSin;
    }

    public void setArcSin(double arcSin) {
        this.arcSin = arcSin;
    }

    public double getArcCos() {
        return arcCos;
    }

    public void setArcCos(double arcCos) {
        this.arcCos = arcCos;
    }

    public double getArcTan() {
        return arcTan;
    }

    public void setArcTan(double arcTan) {
        this.arcTan = arcTan;
    }
}
